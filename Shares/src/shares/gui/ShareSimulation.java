    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        int share = -1;
        if(rbtnA.isSelected()){
            share = 0;
        }
        else share = 1;
        boolean ok = ControlBuySell.processBuyOrder(listCount.getSelectedValue(), share);
        
        if(!ok){
            lNotice.setText(bundle.getString("ErrorTooMuchShares"));
            lNotice.setVisible(true);
        }
        
        //if transaction is fine, than refresh all data
        refreshData();
        if(ok)
            lNotice.setVisible(false);
        
        
    }//GEN-LAST:event_btnBuyActionPerformed

    private void btnSellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSellActionPerformed
        
        int share = -1;
        if(rbtnA.isSelected()){
            share = 0;
        }
        else share = 1;
        boolean ok = ControlBuySell.processSellOrder(listCount.getSelectedValue(), share);
        
        if(!ok){
            lNotice.setText(bundle.getString("ErrorHolding"));
            lNotice.setVisible(true);
        }
        
        //refresh data, if transaction is done
        refreshData();
        if(ok)
            lNotice.setVisible(false);
    }//GEN-LAST:event_btnSellActionPerformed

